package lab2_2;

public class Head {

    private int shape;
    private String colorEyes;

    public Head(int shape, String colorEyes) {
        this.shape = shape;
        this.colorEyes = colorEyes;
    }

    public int getShape() {
        return shape;
    }

    public void setShape(int shape) {
        this.shape = shape;
    }

    public String getColorEyes() {
        return colorEyes;
    }

    public void setColorEyes(String colorEyes) {
        this.colorEyes = colorEyes;
    }
}
