package lab2_2;

public class Leg {

    private int size;
    private int length;

    public Leg(int size, int length) {
        this.size = size;
        this.length = length;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
